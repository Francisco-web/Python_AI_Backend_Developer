

nome = "Francisco é jogador"

print(nome[0]) #- [0]- mostra o caracter na posição 0 (zero) do texto

print(nome[:9]) # [inicio da contagem : fim da contagem] começa da posição 0 ate 9

print(nome[10:]) # [inicio da contagem : fim da contagem] começa da posição 10 ate o fim 

print(nome[10:19])# [inicio da contagem : fim da contagem] começa da posição 10 ate a posição 19

print(nome[10:19:2])# [inicio da contagem : fim da contagem] começa da posição 10 ate 19 e deve dar 2 passos após cada contagem 

print(nome [:])# [inicio da contagem : fim da contagem] isso quer dizer que nenhuma posição foi solicitada, mostra todos os elementos

print(nome[::-1])#espelha a string a ordem alterar da esquerda para direita